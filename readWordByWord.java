public class readWordByWord {
    public static void main(String[] args){
        String name = "Elyas Mohammed Fatani";
        for(int index = 0; index < name.charAt(index); index++){
            if(name.charAt(index) == ' '){
                System.out.println(" ");
            }else{
                System.out.print(name.charAt(index));
            }

        }

    }
}
